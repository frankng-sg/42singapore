#include <unistd.h>

void	ft_print_comb2(void)
{
	char	a[2];
	char	b[2];
	char	i;
	char	j;

	i = 0;
	while (i < 98)
	{
		j = i + 1;
		while (j <= 99)
		{
			a[0] = i / 10 + 48;
			a[1] = i % 10 + 48;
			b[0] = j / 10 + 48;
			b[1] = j % 10 + 48;
			write(1, a, 2);
			write(1, " ", 1);
			write(1, b, 2);
			write(1, ", ", 2);
			j++;
		}
		i++;
	}
	write(1, "98 99", 5);
}
